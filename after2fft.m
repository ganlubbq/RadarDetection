% y_velocity=linspace(0, 1/T*(M-1)/M, M);
% meshx=x_distance(1:N/2)*c/(2*B/T);
% mesh(meshx,c*y_velocity/(2*f0),(abs(fft2(:,1:N/2))));
% title('差频信号二维频谱仿真图');
% xlabel('distance/m');
% ylabel('velocity/(m/s)');
end
