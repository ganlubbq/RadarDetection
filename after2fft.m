function fft2 =after2fft(response,N,fs,T,B,f0)
%返回二维fft变换后的矩阵
c=3e8;
M=size(response,1); %积累周期
fft1=zeros(M,N);
fft2=zeros(M,N);


%一维fft变换
for k=1:M
  fft1(k,:)=fft(response(k,:),N);
end

%二维fff变换
for i=1:N
    fft2(:,i) = fft(fft1(:,i),M);
end
